package switchcase;

public class MobileMenu {

	public static void main(String[] args) {
        int c = 2;

        switch(c) {
            case 1: System.out.println("WiFi"); break;
            case 2: System.out.println("Bluetooth"); break;
            case 3: System.out.println("Mobile Data"); break;
            case 4: System.out.println("Airplane Mode"); break;
        }
    }
}
