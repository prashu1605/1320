package switchcase;

public class Dept {

	public static void main(String[] args) {
        int d = 1;

        switch(d) {
            case 1: System.out.println("Computer Science"); break;
            case 2: System.out.println("Mechanical"); break;
            case 3: System.out.println("Civil"); break;
            case 4: System.out.println("Electrical"); break;
        }
    }
}
