package switchcase;

public class Area {

	public static void main(String[] args) {
        int choice = 1;

        switch(choice) {
            case 1: System.out.println(3.14 * 5 * 5); break;
            case 2: System.out.println(4 * 6); break;
            case 3: System.out.println(5 * 5); break;
        }
    }
}
